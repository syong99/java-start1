public class HelloJava {  //HelloJava 클래스 범위 시작

    /**
     * main 메소드 프로그램을 실행하면 메인을 찾아요
     * [] 배열
     * System.out.println 콘솔에 출력하기 위한 기능
     *  자바는 문자열을 사용할때 "(쌍따음표) 를 사용한다
     *  자바는 한 문장이 끝날때 ;(세미클론)을 필수로 쓴다.
     */
    public static void main(String [] args){
        System.out.println("hello java");
    }
}
